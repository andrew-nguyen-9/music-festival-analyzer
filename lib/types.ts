export interface Tag {
  id: string;
  slug: string;
  label: string;
  type: "genre" | "vibe" | "format" | "region" | "season" | null;
}

/** Cached Spotify data per artist (TTL). Backing store for v2.2; written by the
 *  sync worker (service role), read publicly. `expires_at` is DB-generated. */
export interface ArtistSpotifyCache {
  id: string;
  artist_id: string;
  spotify_id: string | null;
  followers: number | null;
  popularity: number | null; // 0–100
  genres: string[];
  image_url: string | null;
  preview_url: string | null;
  top_tracks: unknown | null; // [{ name, preview_url, ... }]
  raw: unknown | null; // full Spotify payload
  fetched_at: string;
  ttl_seconds: number;
  expires_at: string; // trigger-maintained: fetched_at + ttl_seconds
  created_at: string;
  updated_at: string;
}

// ── Composite / view types used by the UI ──────────────────────

/** A lineup row joined with its artist — what LineupGrid renders. */
export interface LineupEntry extends Lineup {
  artist: Artist;
}

/** An artist's appearance joined with the festival it was at. */
export interface ArtistAppearance extends Lineup {
  festival: Festival;
}

export interface SearchResult {
  type: "festival" | "artist";
  id: string;
  slug: string;
  name: string;
  description: string | null;
  score: number;
}

/** "Did you mean?" suggestion (v3.3) — closest name regardless of threshold. */
export interface Suggestion {
  type: "festival" | "artist";
  slug: string;
  name: string;
  score: number;
}

/** Ingestion run-log row for the observability dashboard (v3.11). */
export interface IngestionRunSummary {
  festival_slug: string | null;
  status: string; // running | success | partial | error
  started_at: string;
  finished_at: string | null;
  rows_upserted: number | null;
  rows_skipped: number | null;
}

/** Editorial guide for a festival (v3.9). body_md is markdown. */
export interface FestivalGuide {
  id: string;
  festival_id: string;
  slug: string;
  title: string;
  body_md: string;
  author: string | null;
  published_at: string | null;
}
