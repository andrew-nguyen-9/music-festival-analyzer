export interface Festival {
  id: string;
  slug: string;
  name: string;
  city: string | null;
  state: string | null;
  country: string | null;
  venue: string | null;
  timezone: string | null; // IANA, e.g. "America/Chicago"; set times are local to this
  start_date: string | null; // ISO date
  end_date: string | null; // ISO date
  website_url: string | null;
  wikipedia_url: string | null;
  description: string | null;
  tags: string[];
  instagram_handle: string | null;
  x_handle: string | null;
  accent_color: string | null; // hex
  hero_image_url: string | null;
  logo_url: string | null;
  vector_art: string | null;       // SVG string generated by Claude
  dates_estimated: boolean | null; // true = start/end dates are estimates
  latitude: number | null;         // venue coordinate (Nominatim)
  longitude: number | null;
  geocoded_at: string | null;      // when lat/lng were resolved
  is_active: boolean;
  created_at: string;
  updated_at: string;
}

export interface Artist {
  id: string;
  slug: string;
  name: string;
  bio: string | null;
  genres: string[];
  origin_city: string | null;
  origin_country: string | null;
  website_url: string | null;
  spotify_id: string | null;
  apple_music_id: string | null;
  spotify_url: string | null;
  apple_music_url: string | null;
  spotify_followers: number | null;
  spotify_popularity: number | null; // 0–100
  preview_url: string | null;
  image_url: string | null;
  header_image_url: string | null;
  tags: string[];
  created_at: string;
  updated_at: string;
}

export interface Lineup {
  id: string;
  festival_id: string;
  artist_id: string;
  year: number;
  stage: string | null;
  day: string | null; // ISO date
  set_time_start: string | null; // local to the festival's timezone
  set_time_end: string | null;
  is_headliner: boolean;
  source: string | null; // provenance/trust: official|wikipedia|ticketmaster|songkick|setlistfm|ocr|estimated
  created_at: string;
}

export interface Media {
  id: string;
  festival_id: string;
  unsplash_id: string | null;
  url_regular: string | null;
  url_thumb: string | null;
  url_full: string | null;
  alt_text: string | null;
  photographer: string | null;
  photographer_url: string | null;
  credit_html: string | null;
  created_at: string;
}

/** Per-festival stage coordinates (v2.3.4). v2.8 joins lineups.stage = stages.name.
 *  coords_source: "known" (published layout) | "venue_centroid" (festival lat/lng spread). */
export interface Stage {
  id: string;
  festival_id: string;
  name: string;
  latitude: number | null;
  longitude: number | null;
  coords_source: string | null;
  created_at: string;
  updated_at: string;
}

export type SocialPlatform = "instagram" | "x";

