-- Upcoming, active festivals with date-status — dashboard helper (added 20260610).
create or replace view v_upcoming_festivals as
  select
    id, slug, name, city, state,
    start_date, end_date, dates_estimated,
    tags, accent_color, hero_image_url, vector_art, is_active
  from festivals
  where is_active = true
    and (start_date is null or start_date >= current_date - interval '30 days')
  order by start_date asc nulls last, name;

