-- ============================================================
-- FESTIVALS
-- ============================================================
create table if not exists festivals (
  id            uuid primary key default uuid_generate_v4(),
  slug          text unique not null,              -- lollapalooza-chicago
  name          text not null,                     -- Lollapalooza
  city          text,
  state         text,
  country       text default 'US',
  venue         text,
  timezone      text,                              -- IANA tz, e.g. 'America/Chicago' (added v2.3.3) — set times are stored local to this
  start_date    date,
  end_date      date,
  website_url   text,
  wikipedia_url text,
  description   text,
  tags          text[] default '{}',               -- ['rock', 'multi-genre', 'outdoor', 'annual']
  instagram_handle text,
  x_handle      text,
  accent_color  text,                              -- hex for festival theming, e.g. '#FF4500'
  hero_image_url text,
  logo_url      text,
  dates_estimated boolean default false,           -- true = start/end are estimates, not yet announced (added 20260610)
  vector_art    text,                              -- raw SVG generated by Claude (added 20260610)
  latitude      double precision,                  -- venue coordinate, geocoded via Nominatim (added v2.3.4)
  longitude     double precision,
  geocoded_at   timestamptz,                       -- when lat/lng were resolved; doubles as the geocode cache key
  is_active     boolean default true,
  created_at    timestamptz default now(),
  updated_at    timestamptz default now()
);

-- ============================================================
-- ARTISTS
-- ============================================================
create table if not exists artists (
  id              uuid primary key default uuid_generate_v4(),
  slug            text unique not null,
  name            text not null,
  bio             text,
  genres          text[] default '{}',
  origin_city     text,
  origin_country  text,
  website_url     text,
  spotify_id      text unique,
  apple_music_id  text,
  spotify_url     text,
  apple_music_url text,
  spotify_followers bigint,
  spotify_popularity int,                          -- 0–100
  preview_url     text,                            -- 30s Spotify preview
  image_url       text,
  header_image_url text,
  tags            text[] default '{}',
  created_at      timestamptz default now(),
  updated_at      timestamptz default now()
);

-- ============================================================
-- LINEUPS (festival × artist join)
-- ============================================================
create table if not exists lineups (
  id            uuid primary key default uuid_generate_v4(),
  festival_id   uuid references festivals(id) on delete cascade,
  artist_id     uuid references artists(id) on delete cascade,
  year          int not null,
  stage         text,                              -- Main Stage, Perry's, etc.
  day           date,
  set_time_start time,                             -- local to festivals.timezone
  set_time_end   time,
  is_headliner  boolean default false,
  source        text,                              -- provenance/trust (added v2.3.3): official|wikipedia|ticketmaster|songkick|setlistfm|ocr|estimated
  created_at    timestamptz default now(),
  unique (festival_id, artist_id, year)
);

