-- ============================================================
-- UPDATED_AT triggers
-- ============================================================
create or replace function set_updated_at()
returns trigger as $$
begin new.updated_at = now(); return new; end;
$$ language plpgsql;

create trigger festivals_updated_at before update on festivals
  for each row execute function set_updated_at();

create trigger artists_updated_at before update on artists
  for each row execute function set_updated_at();

-- artist_spotify_cache maintains expires_at (= fetched_at + ttl_seconds) and
-- updated_at via a trigger — timestamptz+interval is STABLE, so it can't be a
-- GENERATED column.
create or replace function set_artist_cache_expiry()
returns trigger as $$
begin
  new.expires_at := new.fetched_at + make_interval(secs => new.ttl_seconds);
  new.updated_at := now();
  return new;
end;
$$ language plpgsql;

create trigger artist_spotify_cache_expiry
  before insert or update on artist_spotify_cache
  for each row execute function set_artist_cache_expiry();

-- Conflict policy (v2.3.3): a lower-trust writer must never clobber a verified
-- lineup row. The ON CONFLICT DO UPDATE fires this; returning OLD no-ops the
-- update for verified rows. One DB rule covers every writer.
create or replace function protect_verified_lineups()
returns trigger as $$
begin
  if old.source in ('official', 'wikipedia')
     and (new.source is null or new.source not in ('official', 'wikipedia')) then
    return old;
  end if;
  return new;
end;
$$ language plpgsql;

create trigger lineups_protect_verified
  before update on lineups
  for each row execute function protect_verified_lineups();

create trigger stages_updated_at before update on stages
  for each row execute function set_updated_at();
