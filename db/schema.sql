-- ============================================================
-- STAGES (per-festival stage coordinates) — backing store for v2.8
-- ============================================================
-- One row per distinct lineups.stage. v2.8 joins lineups.stage = stages.name.
-- coords_source: 'known' (published layout) | 'venue_centroid' (festival lat/lng spread).
create table if not exists stages (
  id            uuid primary key default uuid_generate_v4(),
  festival_id   uuid not null references festivals(id) on delete cascade,
  name          text not null,
  latitude      double precision,
  longitude     double precision,
  coords_source text,
  created_at    timestamptz default now(),
  updated_at    timestamptz default now(),
  unique (festival_id, name)
);

